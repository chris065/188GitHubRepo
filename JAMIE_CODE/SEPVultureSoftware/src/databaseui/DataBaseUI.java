    public void setTimeLabel()
    {
        Timer SimpleTimer = new Timer(1000, new ActionListener()
        {
           @Override
           public void actionPerformed(ActionEvent ae)
           {
               time.setText(new FormatTime().formatTime(LocalDateTime.now()));
           }
        });
        SimpleTimer.start();
    }
    
    
